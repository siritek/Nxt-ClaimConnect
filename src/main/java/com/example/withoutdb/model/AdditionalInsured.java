package com.example.withoutdb.model;

public class AdditionalInsured {

    private String AdditionalInsuredID;
    private String AdditionalInsuredName;
    private String AdditionalInsuredAddress;
    private String AdditionalInsuredType;


    public void setAdditionalInsuredID(String additionalInsuredID) {
        AdditionalInsuredID = additionalInsuredID;
    }

    public String getAdditionalInsuredName() {
        return AdditionalInsuredName;
    }

    public void setAdditionalInsuredName(String additionalInsuredName) {
        AdditionalInsuredName = additionalInsuredName;
    }

    public String getAdditionalInsuredAddress() {
        return AdditionalInsuredAddress;
    }

    public void setAdditionalInsuredAddress(String additionalInsuredAddress) {
        AdditionalInsuredAddress = additionalInsuredAddress;
    }

    public String getAdditionalInsuredType() {
        return AdditionalInsuredType;
    }

    public void setAdditionalInsuredType(String additionalInsuredType) {
        AdditionalInsuredType = additionalInsuredType;
    }

    public String getAdditionalInsuredID() {
        return AdditionalInsuredID;
    }
}
